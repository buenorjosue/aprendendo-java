    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        // TODO add your handling code here:
        int val = Integer.parseInt(txtValor.getValue().toString());
        
        //Resto da divis√£o por 2
        int res = val % 2;
        lblRes.setText(Integer.toString(res));
        
        //Elevado ao cubo
        double cubo = Math.pow(val, 3);
        lblCubo.setText(String.format("%.0f", cubo));
        
        //Raiz Quadrada
        double quad = Math.sqrt(val);
        lblQuad.setText(String.format("%.2f", quad));
        
        //Raiz Cubica
        double rcubo = Math.cbrt(val);
        lblRCubo.setText(String.format("%.2f", rcubo));
        
        //Valor Absoluto
        int valab = (int) Math.abs(val);
        lblVA.setText(Integer.toString(valab));
        
        panCalc.setVisible(true);
    }//GEN-LAST:event_btnCalcActionPerformed

